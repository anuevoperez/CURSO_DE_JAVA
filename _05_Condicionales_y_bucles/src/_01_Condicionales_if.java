public class _01_Condicionales_if {

    public static void main(String[] args) {

        // Imponemos una condición, si se cumple la condición ejecutará un bloque de código, si no la cumple, ejecutará el otro (el else).

        int edad = 18;

        if(edad <= 18) {
            System.out.println("Es mayor de edad");
        } else {
            System.out.println("Es menor de edad");
        }

    }
}