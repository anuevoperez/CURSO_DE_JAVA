public class _06_Bucles_determinados_for {

    public static void main(String[] args) {

        /*
            En este ejemplo:

            - int c = 0: Se inicializa la variable de control contador en 0.
            - c <= 5: Esta es la condición de continuación. El bucle continuará mientras la condición sea verdadera.
            - c++: Esto se ejecuta después de cada iteración del bucle y aumenta el valor de contador en 1.
         */

        for (int c = 0; c <= 5; c++) {
            System.out.println("Número: " + c);
        }
    }

}
