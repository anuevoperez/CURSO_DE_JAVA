public class _03_PrimitivosCaracter {

    public static void main(String[] args) {

        // char: Representa a caracteres Unicode. Los caracteres, al contrario que las cadenas de Strings, van representadas por comillas simples.
        char a = 'a';
        System.out.println("a = " + a);
        System.out.println("Tipo char corresponde en byte a: " + Character.BYTES);
        System.out.println("Tipo char corresponde en bites a: " + Character.SIZE);
    }

}
